public class Bouquet {

    //Creating several flowers-objects
    private Flower rose;
    private Flower chamomile;
    private Flower cornflower;
    private Flower fern;

    public Flower getRose() {
        return rose;
    }

    public void setRose(Flower rose) {
        this.rose = rose;
    }

    public Flower getChamomile() {
        return chamomile;
    }

    public void setChamomile(Flower chamomile) {
        this.chamomile = chamomile;
    }

    public Flower getCornflower() {
        return cornflower;
    }

    public void setCornflower(Flower cornflower) {
        this.cornflower = cornflower;
    }

    public Flower getFern() {
        return fern;
    }

    public void setFern(Flower fern) {
        this.fern = fern;
    }
}
